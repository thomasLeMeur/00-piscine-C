char	*ft_strcpy(char *dest, char *src)
{
	char	*start;
	int		i;

	i = 0;
	start = dest;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (start);
}
